public class LogicalNOT {
	public void logicalNOT() {
		System.out.println("Logical NOT (!) Operator is used to inverse the condition.");
		System.out.println("Input & Output should be a boolean value.");
		System.out.println();
		System.out.println("e.g.,");
		System.out.println(" Input\tOuput");
		System.out.println(" true\tfalse");
		System.out.println(" false\ttrue");
	}
}
