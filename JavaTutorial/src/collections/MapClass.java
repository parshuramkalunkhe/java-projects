package collections;

public class MapClass {
	public void mapClass() {
		System.out.println("Map is an interface.");
		System.out.println("Map is data structure in which object will be stored in the form of key & value pair.");
		System.out.println("Map interface has many implementing classes such as :");
		System.out.println("\t a) HashMap");
		System.out.println("\t b) LinkedHashMap");
		System.out.println("\t c) TreeMap");
		System.out.println();
		System.out.println("* Inbuilt non-static methods of Map interface.");
		System.out.println("1. To add an Entry");
		System.out.println("  - put(key, value)");
		System.out.println("  - putAll(map)");
		System.out.println();
		System.out.println("2. To remove objects.");
		System.out.println("  - remove(key)");
		System.out.println("  - clear()");
		System.out.println();
		System.out.println("3. To search key and value.");
		System.out.println("  - containsKey(key)");
		System.out.println("  - containsValue(value)");
		System.out.println();
		System.out.println("4. To access / get entry");
		System.out.println("  - entrySet()");
		System.out.println("  - keySet()");
		System.out.println("  - values()");
		System.out.println();
		System.out.println("5. To sort map.");
		System.out.println("  - TreeMap() --> ASC");
		System.out.println("  - descendingMap() --> DESC");
		System.out.println();
	}
}
