package oopsbasic;

public class OOPSBasics {
	public void oopsBasics() {
		System.out.println("Members.");
		System.out.println("1. Variables.");
		System.out.println("2. Methods.");
		System.out.println("Global Members");
		System.out.println("\t Any member declared inside global area are known as global member.");
		System.out.println("\t There are two types of global memmbers.");
		System.out.println("\t 1. Static Members");
		System.out.println("\t 2. Non-Static Members");
		System.out.println();
		System.out.println("1. Static Members");
		System.out.println("\t Any member declared inside global area prefix with static keywords are known as static members.");
		System.out.println("\t a. Static Variables.");
		System.out.println("\t b. Static Methods.");
		System.out.println("\t c. Static Block / SIB (Static Initializer Block)");
		System.out.println();
		System.out.println("2. Non-Static Members");
		System.out.println("\t Any member declared inside global area prefix with non static keywords are known as non-static members.");
		System.out.println("\t a. Non-Static Variable");
		System.out.println("\t b. Non-Static Method");
		System.out.println("\t c. Non-Static Block / IIB (Instance Initializer Block)");
		System.out.println("\t d. Constructor.");
		System.out.println();
		System.out.println("*Q. Difference between Global Variable & Local Variable.");
		System.out.println("\t Local Variable.");
		System.out.println("\t 1. A variable declared inside local area is know as local variable.");
		System.out.println("\t 2. Local variable are not assign with default values.");
		System.out.println("\t 3. Local variable can not be used without initialization.");
		System.out.println("\t 4. Local variables will be stored inside stack area.");
		System.out.println();
		System.out.println("\t Global Variables.");
		System.out.println("\t 1. A variable declared inside global area is know as global variable.");
		System.out.println("\t 2. Global variable are assign with default values.");
		System.out.println("\t 3. Global variable can be used without initialization.");
		System.out.println("\t 4. Global variable will be stored inside class static area.");
		System.out.println();
		System.out.println("* Default valued stored by global variables.");
		System.out.println("byte = 0");
		System.out.println("short = 0");
		System.out.println("int = 0");
		System.out.println("long = 0");
		System.out.println("float = 0.0");
		System.out.println("double = 0.0");
		System.out.println("boolean = false");
		System.out.println("char = nothing/blank");
		System.out.println("String = null");
	}
}
