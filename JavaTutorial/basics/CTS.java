package basics;

public class CTS {
	public void cts() {
		System.out.println("These statements are used to transfer the control from one point to another.");
		System.out.println("There are 3 types of control transfer satements.");
		System.out.println("1. break");
		System.out.println("2. continue");
		System.out.println("3. return");
		System.out.println();
		System.out.println("1. break");
		System.out.println("\t It breaks the current flow of the program at specified condition.");
		System.out.println("\t It is used to transfer the control outside the block.");
		System.out.println("\t It can be used inside switch and loop.");
		System.out.println("\t In case of inner loop, it breaks only inner loop.");
		System.out.println();
		System.out.println("2. continue");
		System.out.println("\t It is used to transfer the control back to the condition.");
		System.out.println("\t It can be used with for loop or while loop.");
		System.out.println("\t The Java continue statement is used to continue the loop.");
		System.out.println("\t It continues the current flow of the program and skips the remaining code at the specified condition.");
		System.out.println("\t In case of an inner loop, it continues the inner loop only.");
		System.out.println();
		System.out.println("3. return");
		System.out.println("\t It is used to return maximum one data from called method. to calling method.");
		System.out.println();
		System.out.println("*RULES : ");
		System.out.println("\t return statement must be the last statement in method.");
		System.out.println("\t one method can have only one return statement.");
	}
}
