package oopsbasic;

public class StaticVariables {
	static int a;
	public void staticVariables() {
		System.out.println("Any variable declared inside global area prefix with static keyword are known as static variable.");
		System.out.println("Any global variable prefix with static keyword known as static variable.");
		System.out.println();
		System.out.println("e.g., static int a; // declared inside global area.");
		System.out.println("global variables assign with default value so the value of a is : " + a);
	}
}
